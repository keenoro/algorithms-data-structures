// FIBONACCI SERIES
// Print the n-th number in the fibnoacci series
// There was 3 ways to solve it
// Iterative, recursion, memoization

// Solution #3, memoization solution (improved recursion) O(n)
function memoize(fn) {
  const cache = {};
  return function(...args) {
    if (cache[args]) {
      return cache[args];
    }

    const result = fn.apply(this, args);
    cache[args] = result;

    return result;
  };
}

